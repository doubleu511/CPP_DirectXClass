#include "pch.h"
#include "Arithmetic.h"

double MathLibrary::Arithmetic::Add(double a, double b)
{
    return a + b;
}

double MathLibrary::Arithmetic::Sub(double a, double b)
{
    return a - b;
}

double MathLibrary::Arithmetic::Mul(double a, double b)
{
    return a * b;
}

double MathLibrary::Arithmetic::Div(double a, double b)
{
    return a / b;
}
